    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        
        String item = (String) this.item.getSelectedItem();
        String requestBy = (String) this.requestBy.getSelectedItem();
        LocalDateTime date = LocalDateTime.now(); // Current date and time
        
        // Format the date to a string
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        String formattedDate = date.format(formatter);
        Request request = new Request(id.getText(),item, requestBy, formattedDate);
        request.addData();
    }//GEN-LAST:event_addActionPerformed

