    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(tableModel);
        jTable1.setRowSorter(sorter);

        // Check if `searchTextField` is initialized
        if (searchTextField == null) {
            JOptionPane.showMessageDialog(this, "Search field is not initialized!");
            return;
        }

        String searchText = searchTextField.getText();
        if (searchText.trim().length() == 0) {
            sorter.setRowFilter(null);
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + searchText));
        }
    }


    public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        // Ensure `asset` is initialized
        if (asset == null) {
            asset = new Asset();
        }

        // Fetch data and populate the table
        List<Object[]> allAssetDetails = asset.getData();
        for (Object[] row : allAssetDetails) {
            model.addRow(row);
        }
    }


